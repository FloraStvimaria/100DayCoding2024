public class EvaluasiMentor3 {
    public static void main(String[] args) {
        int f = 6;
        int s,y;
        for (s = f; s >= 1; s--) {
            for (y = 1; y <= s; y++) {
                System.out.print(y + " ");
            }
            System.out.println();
        }
    }
    
}
